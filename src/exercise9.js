function adjustLights(lights) {
  let current = '游릭'
  let errors = 0
  for(const light of lights){
    errors = (light === current)? errors : errors + 1
    current = (current ==='游릭')? '游댮': '游릭'
  }
  if(errors > lights.length / 2) errors = lights.length - errors
  return errors
}

